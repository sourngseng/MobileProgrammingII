// using if else statement in dart
void main() {
  int score = 85;

  if (score >= 80) {
    print("Congratulations! You passed with an A grade.");
  } else {
    print("You did not pass with an A grade. Better luck next time!");
  }

  print("---------------------------------------------------");
  print(
    "This statement is outside the if-else block and will always be executed.",
  );
}
